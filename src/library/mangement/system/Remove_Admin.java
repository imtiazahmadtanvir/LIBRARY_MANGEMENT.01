    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = this.name.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter username.");
            return;
        }
        try {
            DBConnection c = new DBConnection();
            java.sql.ResultSet rs = c.s.executeQuery("select * from login where username = '" + name + "'");
            if (rs.next()) {
                c.s.executeUpdate("delete from login where username = '" + name + "'");

                JOptionPane.showMessageDialog(null, "Admin deleted successfully.");
                setVisible(false);
                new Dashboard().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Admin is not available for this username.");
            }

        } catch (HeadlessException | SQLException e) {
            String errorMessage = "SQL Exception: " + e.getMessage();
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton1ActionPerformed

