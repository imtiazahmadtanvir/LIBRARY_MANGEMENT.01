    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int stuffID = Integer.parseInt(this.stuffID.getText());
        if (this.stuffID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Struff ID");
            return;
        }
        
        try {
            DBConnection c = new DBConnection();
            String query = "delete from staff where stuffID = '"+stuffID+"'";
            c.s.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Delete successfully");
            dispose();
            new Dashboard().setVisible(true);
            
        } catch (SQLException e) {
            String errorMessage = "SQL Exception: " + e.getMessage();
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

