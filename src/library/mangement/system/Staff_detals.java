    public Staff_detals() {
        initComponents();
        DBConnection c = new DBConnection();

        DefaultTableModel TBModel = (DefaultTableModel) Table.getModel();

        TBModel.setRowCount(0);
        try {
            String q = "SELECT * FROM staff";
            java.sql.ResultSet rs1 = c.s.executeQuery(q);
            int i = 0;
            while (rs1.next()) {
                // Add a new row to the model
                TBModel.addRow(new Object[]{});

                // Set the value at the specified column and row
                TBModel.setValueAt(rs1.getInt("stuffID"), i, 0);
                TBModel.setValueAt(rs1.getString("name"), i, 1);
                TBModel.setValueAt(rs1.getString("contact"), i, 2);
                i++;
            }
        } catch (SQLException e) {
            String errorMessage = "SQL Exception: " + e.getMessage();
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

