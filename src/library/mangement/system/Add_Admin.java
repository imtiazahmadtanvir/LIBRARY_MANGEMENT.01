    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = this.name.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter username.");
            return;
        } else {
            try {
                DBConnection c = new DBConnection();
                String query = "SELECT username FROM login WHERE username = '" + name + "'";
                java.sql.ResultSet rs = c.s.executeQuery(query);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "This username already taken, " + "\n" + "Try with Uniqe username");
                    return;
                }
            } catch (HeadlessException | SQLException e) {
                String errorMessage = "SQL Exception: " + e.getMessage();
                JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        String pass = this.pass.getText();
        if (pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Password.");
            return;
        }

        try {
            DBConnection c = new DBConnection();
            String query = "Insert into login (username, pass) values ('" + name + "','" + pass + "')";
            c.s.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "New admin added");
            setVisible(false);
            new Dashboard().setVisible(true);
        } catch (HeadlessException | SQLException e) {
            String errorMessage = "SQL Exception: " + e.getMessage();
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton3ActionPerformed

