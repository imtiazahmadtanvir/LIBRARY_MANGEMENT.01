    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String catagory = this.catagory.getText();
        if (catagory.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Book Id");
            return;
        }
        String name = this.name.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Book Id");
            return;
        }
        String author = this.author.getText();
        if (author.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Book Id");
            return;
        }
        int copies = Integer.parseInt(copy.getText());
        if (copy.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Book Id");
            return;
        }
        
        try {
            DBConnection c = new DBConnection();
            String query = "Insert into books (catagory, name, author, copies) values ('"+catagory+"','"+name+"','"+author+"','"+copies+"')";
            c.s.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Update successfully");
            dispose();
            new Dashboard().setVisible(true);
            
        } catch (SQLException e) {
            String errorMessage = "SQL Exception: " + e.getMessage();
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

